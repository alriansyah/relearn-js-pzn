/*
    Operator Perbandingan :
    < (Kurang dari)
    > (Lebih dari)
    <= (Kurang dari sama dengan)
    >= (Lebih dari sama dengan)
    == (Sama dengan)
    === (Sama dengan dan sama tipe data)
    != (Tidak sama dengan)
    !== (Tidak sama dengan dan tidak sama tipe data)
*/

const number1 = 2;
const number2 = 3;

console.log(number1 < number2);
console.log(number1 > number2);
console.log(number1 <= number2);
console.log(number1 >= number2);
console.log(number1 == number2);
console.log(number1 === number2);
console.log(number1 != number2);    
console.log(number1 !== number2);