/*
    Operator Logika :
    && (Logical AND)
    || (Logical OR)
    ! (Logical NOT)
*/

// AND
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);
console.log("----------");


// OR
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);
console.log("----------");

// NOT
console.log(!true);
console.log(!false);
console.log("----------");

/*
    Operator Unary :
    ! (Unary Not)
*/
console.log(!true);
console.log(!false);




