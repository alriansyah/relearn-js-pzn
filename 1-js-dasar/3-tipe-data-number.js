/*
JavaScript mendukung number notation berikut :
- Defaultnya berbasis decimal / 10
- Hexadecimal = 0x
- Binary = 0b
- Octal = 0o
*/

const decimal = 100;
const hexadecimal = 0x100;
const binary = 0b100;
const octal = 0o100;

console.log(decimal);
console.log(hexadecimal);
console.log(binary);
console.log(octal);
console.log("--------------------------");
console.log(typeof decimal);
console.log(typeof hexadecimal);
console.log(typeof binary);
console.log(typeof octal);
