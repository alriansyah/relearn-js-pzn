// Switch merupakan salah satu statement di javascript yang mirip sama dengan if else
// namun lebih sederhana pembuatannya
// Switch case hanya untuk perbandingan ==
const nilai = "C";

switch (nilai) {
    case "A":
        console.log("Nilai sangat baik");
        break;
    case "B":
        console.log("Nilai baik");
        break;
    case "C":
        console.log("Nilai cukup");
        break;
    default:
        console.log("Maaf anda tidak lulus");
}
