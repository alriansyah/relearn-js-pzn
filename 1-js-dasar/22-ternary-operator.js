// Ternary Operator : operasi yang lebih sederhana dari if else /  if statement
let nama = "Al Riansyah";
nama === undefined ? console.log("Undefined") : console.log(nama);

const result = nama === undefined ? "Undefined" : nama;
console.log(result);

const ucapan = nama === undefined ? "Undefined" : "Hello Kamu";
console.log(ucapan);